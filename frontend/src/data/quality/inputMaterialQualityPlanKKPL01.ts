export function qualityPlanRowsForCategory(
  category: QualityPlanMaterialCategory,
): InputMaterialQualityPlanRow[] {
  return KK_PL01_QUALITY_PLAN_ROWS.filter((r) => r.materialCategory === category)
}

/** Malzeme türüne göre KK-PL-01 plan satırları için specValues şablonu (mevcut değerleri korur). */
export function specValuesForMaterialType(
  type: InputMaterialType,
  existing?: Record<string, string>,
): Record<string, string> {
  const rows = qualityPlanRowsForCategory(mapInputMaterialTypeToPlanCategory(type))
  const next: Record<string, string> = {}
  for (const row of rows) {
    next[row.id] = existing?.[row.id] ?? ''
  }
  return next
}

export function mapInputMaterialTypeToPlanCategory(
  type: InputMaterialType,
): QualityPlanMaterialCategory {
  const map: Record<InputMaterialType, QualityPlanMaterialCategory> = {
    rebar: 'rebar',
    mesh: 'mesh',
    prestress: 'prestress_steel',
    accessory: 'auxiliary',
    cement: 'cement',
    aggregate: 'aggregate',
    powder_paint: 'powder_paint',
    form_oil: 'form_oil',
    chemical_admixture: 'chemical_admixture',
    steel_form: 'steel_form',
    other: 'auxiliary',
  }
  return map[type] ?? 'auxiliary'
}
