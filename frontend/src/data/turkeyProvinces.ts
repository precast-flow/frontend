export function getDistrictsForProvince(provinceName: string): string[] {
  const found = TURKEY_PROVINCES.find((p) => p.name === provinceName)
  return found?.districts ?? [...MERKEZ_ONLY]
}

export function getProvinceNames(): string[] {
  return TURKEY_PROVINCES.map((p) => p.name)
}
